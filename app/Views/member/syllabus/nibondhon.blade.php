<h2>বাংলা</h2>

<p>স্কুল ও স্কুল পর্যায়-২-এ বাংলা অংশে ভালো করতে হলে ব্যাকরণে জোর দিতে হবে । ব্যাকরণের প্রায় প্রতিটি অধ্যায় থেকে এক থেকে দুটি প্রশ্ন আসে। এসব অধ্যায়গুলোর মধ্যে ভাষারীতি ও বিরাম চিহ্নের ব্যবহার, বাগধারা ও বাগবিধি, ভুল সংশোধন বা শুদ্ধকরণ, অনুবাদ, সন্ধিবিচ্ছেদ, কারক, বিভক্তি, সমাস ও প্রত্যয়, সমার্থক ও বিপরীতধর্মী শব্দ, বাক্য সংকোচন, লিঙ্গ পরিবর্তন অধ্যায়গুলো ভালোভাবে পড়লে প্রশ্ন পাওয়া যাবে। আর কলেজ পর্যায়ের জন্য পড়তে হবে নবম থেকে দ্বাদশ শ্রেণির পাঠ্যবইগুলো। এসব বইয়ের গদ্য ও পদ্যের লেখক পরিচিতি সম্পর্কে জানা থাকলে ভালো করা যাবে। এ ছাড়া বিগত বছরের শিক্ষক নিবন্ধন পরীক্ষার প্রশ্নের সমাধান করলেও বেশ কাজে দেবে।<br />
স্কুল ও কলেজ-উভয় পরীক্ষায় বেশির ভাগ প্রশ্নই করা হয় ব্যাকরণ থেকে। ভাষারীতি ও বিরামচিহ্ন, সারসংক্ষেপ, ভাবসম্প্রসারণ, বাগধারা ও বাগবিধি, পত্রলিখন, ভুল সংশোধন ও সমাস-এ বিষয়গুলো থেকে প্রশ্ন করা হয় স্কুল পর্যায়ের পরীক্ষায়। কলেজ পর্যায়ে এসব বিষয়ের পাশাপাশি সমার্থক শব্দ থেকে প্রশ্ন করা হয়। অষ্টম শিক্ষক নিবন্ধন পরীক্ষায় উত্তীর্ণ নাজমুল হোসেন জানান, বাংলা বিষয়ের প্রস্তুতির জন্য জাতীয় শিক্ষাক্রম ও পাঠ্যপুস্তক বোর্ড প্রণীত নবম-দশম শ্রেণীর বাংলা ব্যাকরণ বইটি বেশ কাজে দেবে। এ ছাড়া বোর্ড অনুমোদিত মাধ্যমিক পর্যায়ের অন্যান্য বাংলা ব্যাকরণ বই পড়লে উত্তর করা সহজ হবে।</p>

