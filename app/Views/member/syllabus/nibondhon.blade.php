নির্ধারিত সময়ের মধ্যে আবেদনপত্রের হার্ডকপি উক্ত জি.পি.ও. বক্সে পৌছাতে ব্যর্থ হলে সংশ্লিষ্ট প্রার্থীর প্রার্থীতা বাতিল হবে। আবেদনপত্রের হার্ডকপি কোনক্রমেই হাতে হাতে, রেজিস্ট্রি ডাকযোগে কিংবা কুরিয়ার সার্ভিসের মাধ্যমে এনটিআরসিএ-তে গ্রহণ করা হবে না। . প্রিলিমিনারি টেস্টে উত্তীর্ণ প্রার্থীদের হার্ডকপি প্রেরণের ড়েগত্রে খামের উপরে &ldquo;দ্বাদশ শিক্ষক নিবন্ধন পরীক্ষার আবেদনপত্র&rdquo; লিখতে হবে। হার্ডকপি পুঙ্খানুপুঙ্খ যাচাইয়ের পর শুধু ত্রশ্নটিমুক্ত আবেদনপত্রের বিপরীতে লিখিত পরীড়া প্রদানের অনুমতি দেয়া হবে। পিলিমিনারি টেস্টে পাস করা লিখিত পরীড়গায় অবতীর্ণ হওয়ার একমাত্র যোগ্যতা নয়। প্রিলিমিনারি টেস্টে পাস করা যে সকল প্রার্থী হার্ডকপি নির্ধারিত তারিখের মধ্যে জমা দিতে ব্যথ হবেন তাদের প্রার্থিতা বাতিল বলে গণ্য হবে এবং তারা লিখিত পরীক্ষায় অংশগ্রহণ করতে পারবেন না । যে সকল প্রার্থী প্রিলিমিনারি টেস্টে উত্তীর্ণ হবেন এবং যাদের হার্ডকপি সম্পূর্ণরূপে ত্রমটিমুক্ত পাওয়া যাবে শুধু তারাই দ্বাদশ শিড়াক নিবন্ধন পরীড়া ২০১৫-এর লিখিত পরীক্ষায় অংশগ্রহণ করতে পারবেন।</p>

<p>পর্যায়সমূহ:</p>

<p>ক. স্কুল পর্যায় বলতে সহকারী শিক্ষক (বাংলা, ইংরেজি, সামাজিক বিজ্ঞান, ব্যবসায় শিক্ষা, গণিত ও সাধারণ বিজ্ঞান, বিজ্ঞান, কৃষি, গার্হস্থ্য বিজ্ঞান, কম্পিউটার ও ধর্ম), সহকারী শিক্ষক (শরীরচর্চা)/ শরীরচর্চা শিক্ষক, সহকারী মৌলবি, এবতেদায়ি প্রধান, প্রদর্শক ও সহকারী শিক্ষক (সাধারণ বিষয়-ভাষা) পদকে বুঝাবে।</p>

<p>খ. স্কুল পর্যায়-২ বলতে ট্রেড ইন্সট্রাক্টর, জুনিয়র মৌলবি, জুনিয়র শিক্ষক (সাধারণ) ও এবতেদায়িকারী পদকে বুঝাবে।</p>

<p>গ. কলেজ পর্যায় বলতে প্রভাষক যা বিজ্ঞপ্তির ক্রমিক নং ১১-১৭ (বিষয় নং ১-৩৫) পদকে বুঝাবে।<br />
পরীক্ষার নির্ধারিত সময়ের মধ্যে আবেদনপত্রের হার্ডকপি উক্ত জি.পি.ও. বক্সে পৌছাতে ব্যর্থ হলে সংশ্লিষ্ট প্রার্থীর প্রার্থীতা বাতিল হবে। আবেদনপত্রের হার্ডকপি কোনক্রমেই হাতে হাতে, রেজিস্ট্রি ডাকযোগে কিংবা কুরিয়ার সার্ভিসের মাধ্যমে এনটিআরসিএ-তে গ্রহণ করা হবে না।</p>

<p>&nbsp;প্রিলিমিনারি টেস্টে উত্তীর্ণ প্রার্থীদের হার্ডকপি প্রেরণের ক্ষেত্রে খামের উপরে &ldquo;_____ শিক্ষক নিবন্ধন পরীক্ষার আবেদনপত্র&rdquo; লিখতে হবে। হার্ডকপি পুঙ্খানুপুঙ্খ যাচাইয়ের পর শুধু ত্রশ্নটিমুক্ত আবেদনপত্রের বিপরীতে লিখিত পরীড়া প্রদানের অনুমতি দেয়া হবে। পিলিমিনারি টেস্টে পাস করা লিখিত পরীড়গায় অবতীর্ণ হওয়ার একমাত্র যোগ্যতা নয়। প্রিলিমিনারি টেস্টে পাস করা যে সকল প্রার্থী হার্ডকপি নির্ধারিত তারিখের মধ্যে জমা দিতে ব্যথ হবেন তাদের প্রার্থিতা বাতিল বলে গণ্য হবে এবং তারা লিখিত পরীক্ষায় অংশগ্রহণ করতে পারবেন না । যে সকল প্রার্থী প্রিলিমিনারি টেস্টে উত্তীর্ণ হবেন এবং যাদের হার্ডকপি সম্পূর্ণরূপে ত্রমটিমুক্ত পাওয়া যাবে শুধু তারাই নিবন্ধন পরীড়া -এর লিখিত পরীক্ষায় অংশগ্রহণ করতে পারবেন।</p>

<p>পর্যায়সমূহ:</p>

<p>ক. স্কুল পর্যায় বলতে সহকারী শিক্ষক (বাংলা, ইংরেজি, সামাজিক বিজ্ঞান, ব্যবসায় শিক্ষা, গণিত ও সাধারণ বিজ্ঞান, বিজ্ঞান, কৃষি, গার্হস্থ্য বিজ্ঞান, কম্পিউটার ও ধর্ম), সহকারী শিক্ষক (শরীরচর্চা)/ শরীরচর্চা শিক্ষক, সহকারী মৌলবি, এবতেদায়ি প্রধান, প্রদর্শক ও সহকারী শিক্ষক (সাধারণ বিষয়-ভাষা) পদকে বুঝাবে।</p>

<p>খ. স্কুল পর্যায়-২ বলতে ট্রেড ইন্সট্রাক্টর, জুনিয়র মৌলবি, জুনিয়র শিক্ষক (সাধারণ) ও এবতেদায়িকারী পদকে বুঝাবে।</p>

<p>গ. কলেজ পর্যায় বলতে প্রভাষক যা বিজ্ঞপ্তির ক্রমিক নং ১১-১৭ (বিষয় নং ১-৩৫) পদকে বুঝাবে। উত্তরপত্র সংক্রান্তু গোপনীয়তা: প্রিলিমিনারি টেস্টের MCQ উত্তরপত্র ও লিখিত পরীক্ষার সকল উত্তরপত্র গোপনীয় দলিল হিসেবে গণ্য হবে এবং কোনো অবস্থাতেই কোনো প্রার্থী বা তার প্রতিনিধিকে সেগুলো প্রদর্শন বা প্রদান করা হবে না।</p>

<p>বাংলা, ইংরেজি ও অন্যান্য ভাষা সম্পর্কিত বিষয়ের প্রশ্নের উত্তর সংশিক্ষষ্ট ভাষাতেই লিখতে হবে। অন্যান্য বিষয়ের প্রশ্নের উত্তর বাংলা বা ংরেজি-এর যে কোনো একটিতে লিখা যাবে। একটি বিষয়ের উত্তরে একাধিক ভাষা ব্যবহার করা যাবে না। কোনো বিষয়ের প্রশ্নপত্রে অন্য কোনোরূপ নির্দেশনা থাকলে উক্ত বিষয়ের ক্ষেত্রে ঐ নির্দেশনা অনুযায়ী প্রশ্নোত্তর লিখতে হবে।</p>

<p>পরীক্ষার কেন্দ্রসমূহ: ক. প্রবেশপত্রে উলিস্নখিত পরীক্ষা কেন্দ্রে উপস্থিত হয়ে প্রার্থীকে পরীক্ষা দিতে হবে। কেন্দ্র পরিবর্তনের কোনো আবেদন গ্রহণ করা হবে না |</p>

<p>খ. প্রিলিমিনারি টেস্টের কেন্দ্র দেশের ২০ (বিশ)টি জেলা শহর তথা: দিনাজপুর, রংপুর, বগুড়া, রাজশাহী, পাবনা, কুষ্টিয়া, যশোর, খুলনা, জামালপুর, ময়মনসিংহ, টাঙ্গাইল, ঢাকা, ফরিদপুর, কুমিল্লা, নোয়াখালী, চট্টগ্রাম , বরিশাল, পটুয়াখালী, সিলেট ও রাঙ্গামাটিতে আলোচ্য পরীক্ষা অনুষ্ঠিত হবে।</p>

<p>&nbsp;গ. লিখিত পরীক্ষার কেন্দ্র: প্রিলিমিনারি টেস্টে উত্তীর্ণ প্রার্থীদের দেশের বিভাগীয় ৭টি জেলা শহর তথা: রংপুর, রাজশাহী, খুলনা, ঢাকা, চট্টগ্রাম, বরিশাল ও সিলেট-এ লিখিত পরীক্ষা অনুষ্ঠিত হবে।</p>

<p>&nbsp;ঘ. Online আবেদনপত্রে প্রার্থী কর্তৃক পূরণকৃত পরীক্ষা কেন্দ্রে প্রার্থী প্রিলিমিনারি টেস্টে অংশগ্রহণ করবেন।</p>

<p>বিবিধ: ক. ইতোপূর্বে অনুষ্ঠিত নিবন্ধন পরীক্ষায় উত্তীর্ণ প্রার্থী যোগ্যতাসাপেক্ষে _____ শিক্ষক নিবন্ধন পরীক্ষা ২০১৫-এ অন্য বিষয়ে অংশগ্রহণ করতে পারবেন ।</p>

<p>খ. ফলাফল প্রকাশের তারিখ হতে পরবর্তী ৯০ (নব্বই) দিনের মধ্যে উত্তীর্ণ প্রার্থীর সনদপত্র Online আবেদনপত্রে উল্লেখিত তার স্থায়ী ঠিকানার জেলা শিক্ষা অফিসে প্রেরণ করা হবে। উক্ত সময়ের মধ্যে এনটিআরসিএ কোন সাময়িক প্রত্যয়নপত্র প্রদান করবে না। ওয়েবসাইটে প্রদত্ত ফলাফল এর প্রিন্টকপি সাময়িক প্রত্যয়নপত্র হিসেবে গণ্য হবে।</p>

<p>&nbsp;গ. অনিবার্য কারণবশত: পরীক্ষার তারিখ পরিবর্তিত হলে তা যথানিয়মে এবং যথাসময়ে অবহিত করা হবে। ঘ. বিজ্ঞপ্তিতে অনুলিস্নখিত এমন কোনো বিষয় থাকলে অথবা ব্যাখ্যার প্রয়োজন হলে এ বিষয়ে NTRCA প্রয়োজনীয় ব্যাখ্যা দিবে।</p>

<p>ঙ. পরীক্ষা সংক্রান্ত যাবতীয় তথ্য NTRCA-এর ৯৬১৩৮৩৫, ৯৬১৩০২৯, ৯৬১৫৭৫০, ৯৬১২৯৪৩ ও ৯৬৬৮০৫১ নম্বর টেলিফোন থেকে জানা যাবে |</p>

<p>চ. পরীক্ষা গ্রহণ ও নিবন্ধন সংক্রান্ত যে কোন বিষয়ে কর্তৃপক্ষের সিদ্ধান্তই চূড়ান্ বলে গণ্য হবে।</p>

<p>(মোঃ হুমায়ূন কবীর) সদস্য (যুগ-সচিব) পরীক্ষা মূল্যায়ন ও প্রত্যয়ন এনটিআরসিএ, ঢাকা ।</p>


