<h1>লিখিত পরীক্ষা</h1>

<p>প্রিলিমিনারি পরীক্ষায় উত্তীর্ণ প্রার্থীদের ঐচ্ছিক বিষয়ের ওপর ১০০ নম্বরের তিন ঘণ্টার লিখিত পরীক্ষায় অংশ নিতে হবে। স্কুল পর্যায়ের জন্য নবম-দশম শ্রেণির বইগুলো পড়তে হবে এবং কলেজ পর্যায়ের লিখিত পরীক্ষায় ভালো করতে হলে অনার্স পর্যায়ের বইগুলো পড়লেই চলবে। এ ছাড়া বিগত বছরের প্রশ্ন দেখলেও ধারণা পাওয়া যাবে। লিখিত পরীক্ষায় রচনামূলক প্রশ্ন থাকে পাঁচটি। প্রতিটি প্রশ্নের মান থাকে ১৫। সংক্ষিপ্ত প্রশ্ন থাকবে পাঁচটি। প্রতিটি প্রশ্নেরই বিকল্প প্রশ্ন থাকবে।</p>

