<h3>গণিত</h3>

<p>অনেকেই গণিতে খারাপ করেন। গণিতে ভালো করতে হলে সপ্তম থেকে দশম শ্রেণির বইগুলো বারবার চর্চা করতে হবে। পাটিগণিত থেকে লসাগু, গসাগু, ঐকিক নিয়ম, শতকরা, সুদকষা, লাভ-ক্ষতি, অনুপাত-সমানুপাত&mdash;এসব অধ্যায় ভালো করে চর্চা করলে প্রশ্ন পাওয়া যাবে। আর বীজগণিতের জন্য করতে হবে উৎপাদক, বর্গ ও ঘণ-সংবলিত সূত্রগুলো ও প্রয়োগ, গসাগু, সূচক, লগারিদম&mdash;এসব অধ্যায় থেকে প্রতিবছরই প্রশ্ন থাকে। জ্যামিতির জন্য রেখা, কোণ, ত্রিভুজ, চতুর্ভুজ, ক্ষেত্রফল ও বৃত্ত অধ্যায়গুলো আয়ত্তে রাখা দরকার। কলেজ পর্যায়ের জন্য পড়তে হবে একাদশ ও দ্বাদশ শ্রেণির বই। বিগত বছরগুলোর বিসিএস প্রশ্ন ও শিক্ষক নিবন্ধন প্রশ্নগুলো বারবার চর্চা করলেও ভালো করা যাবে। প্রতিটি উত্তর দেওয়ার ক্ষেত্রে খেয়াল রাখতে হবে।<br />
গণিত অনেকের কাছেই তুলনামূলকভাবে কঠিন। চর্চা করলে এ বিষয়েই সবচেয়ে ভালো নম্বর তোলা সম্ভব। সরল, গড়, ল.সা.গু, গ.সা.গু, শতকরা, সুদকষা, লাভ-ক্ষতি, অনুপাত, সমানুপাত, উৎপাদক, বর্গ ও ঘন সম্পর্কিত সূত্রাবলি ও প্রয়োগ, সেট, ফাংশন, সূচক, লগারিদম, জ্যামিতি, ত্রিকোণমিতি, পরিমিতি-এসব বিষয় থেকে প্রশ্ন করা হয় স্কুল ও কলেজ পর্যায়ের পরীক্ষায়। নাজমুল হোসেন জানান, পাটিগণিত, বীজগণিত, জ্যামিতি, ত্রিকোণমিতি, পরিমিতি-গণিতের প্রতিটি অংশে জোর দিতে হবে। সূত্রগুলো মুখস্ত রাখতে হবে, নিয়মিত চর্চা করতে হবে। বিগত বছরের প্রশ্ন পর্যালোচনা করে দেখা গেছে, গণিতে কিছু সমস্যা থাকে, যার সমাধানে অনেক সময় লেগে যায়। কৌশলী না হলে নির্ধারিত সময়ের মধ্যে পরীক্ষা শেষ করা যায় না। এর জন্য জানতে হবে সংক্ষপ্তি পদ্ধতি।</p>

<h3>সাধারণ জ্ঞান</h3>

<p>সাধারণ ২৫টি প্রশ্ন থাকবে। এ অংশে ভালো করতে হলে নিয়মিত পত্রিকা পড়া, দেশি-বিদেশি সমসাময়িক খবরগুলো নিজের আয়ত্তে করে নেওয়া। বিজ্ঞান, তথ্য ও প্রযুক্তি, পরিবেশ এবং রোগব্যাধি সম্পর্কে ধারণা রাখতে হবে। বাংলাদেশ অংশে বাংলাদেশের ইতিহাস, মুক্তিযুদ্ধ, জলবায়ু, সংস্কৃতি, খেলাধুলা, বিভিন্ন জেলার আয়তন, অর্থনীতি প্রভৃতি সম্পর্কে অবগত থাকা। আর আন্তর্জাতিক অংশের জন্য বিভিন্ন দেশের মুদ্রা, দিবস, পুরস্কার ও সম্মাননা, সাম্প্রতিক ঘটনা এসব থেকে দুই থেকে চারটি প্রশ্ন পাওয়া যেতে পারে। এ ছাড়া বাজারে সাধারণ জ্ঞানের বিভিন্ন প্রকাশনীর বই পড়লেও ভালো করা যাবে।<br />
অন্য তিনটি বিষয়ের তুলনায় সাধারণ জ্ঞানের পরিধি বড়। সিলেবাসও সুনির্দিষ্ট নয়। এ অংশে মূলত বাংলাদেশ ও আন্তর্জাতিক বিষয়াবলি, দৈনন্দিন বিজ্ঞান ও প্রযুক্তি, পরিবেশ ও চিকিৎসাবিজ্ঞান এবং সাম্প্রতিক বিষয়াবলি থেকে প্রশ্ন করা হয়। বাংলাদেশ অংশে প্রাচীন বাংলার ইতিহাস, ভাষা আন্দোলন থেকে মুক্তিযুদ্ধ, বাংলাদেশের ভূপ্রকৃতি, ভৌগোলিক অবস্থান, গুরুত্বপূর্ণ স্থাপনা, নদ-নদী, খাল-বিল, কৃষি, খনিজ সম্পদ বিষয়ে প্রশ্ন আসে। আন্তর্জাতিক বিষয়াবলি থেকে বিভিন্ন দেশের রাজধানী, মুদ্রা, আন্তর্জাতিক দিবস, পুরস্কার, গোয়েন্দা সংস্থা, স্বাধীনতাকামী সংগঠন, খেলাধুলা, জাতিসংঘ ও এর অঙ্গসংগঠন, বিভিন্ন আন্তর্জাতিক ও আঞ্চলিক সংস্থা, আন্তর্জাতিক সীমারেখা বিষয়ে প্রশ্ন থাকে। দৈনন্দিন বিজ্ঞান, কম্পিউটার ও তথ্যপ্রযুক্তি, চিকিৎসাবিজ্ঞান বিষয়ে মৌলিক জ্ঞান থাকলেই উত্তর করা যায়। মাধ্যমিক পর্যায়ের সামাজিক বিজ্ঞান ও সাধারণ বিজ্ঞানের বই পড়লে এ বিষয়ে ভালো করা যায়। সাম্প্রতিক ঘটনাবলি জানার জন্য নিয়মিত পড়তে হবে দৈনিক পত্রিকা।</p>

